package chabernac.protocol.keyexchange;

public class KeyException extends Exception {

  private static final long serialVersionUID = -7652175821412384465L;

  public KeyException() {
    super();
  }

  public KeyException(String anArg0, Throwable anArg1) {
    super(anArg0, anArg1);
  }

  public KeyException(String anArg0) {
    super(anArg0);
  }

  public KeyException(Throwable anArg0) {
    super(anArg0);
  }

}
